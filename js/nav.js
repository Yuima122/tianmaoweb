$(document).ready(
    function () {
        $("#dropDown1").mouseover(
            function () {
                $(".dropDown-menu").show();
                $("#dropDown1").css({"background-color":"white",
                    "border-left":"1px solid #c6c6c6",
                    "border-right":"1px solid #c6c6c6"});
                $("#dropDown1 b").css({"transition":"0.5s",
                    "transform":"rotate(180deg)"});


            }
        )

    }
);
$(document).ready(
    function () {
        $("#dropDown1").mouseout(
            function () {
                $(".dropDown-menu").hide();
                $("#dropDown1").css({"background-color":"#f2f2f2",
                    "border-left":"1px solid #f2f2f2",
                    "border-right":"1px solid #f2f2f2"});
                $("#dropDown1 b").css("transform","rotate(0deg)");

            }
        )

    }
);
