$(document).ready(
    function () {
        $("#icon1").mouseover(
            function () {
                $("#icon1").css("background-color","#FF0036");
                $("#promptBox2").css({"display":"block","animation":"appear 0.5s","animation-fill-mode":"forwards"})

            }
        )

    }
);
$(document).ready(
    function () {
        $("#icon1").mouseout(
            function () {
                $("#icon1").css("background-color","#000");
                $("#promptBox2").css({"animation":"disappear 0.5s","animation-fill-mode":"forwards"});

            }
        )

    }
);
